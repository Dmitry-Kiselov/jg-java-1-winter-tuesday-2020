package l7;

//Ant
public class Murovey {

    private String name;

    public Murovey(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    @Override
    public String toString() {
        return "Murovey{" +
                "name='" + name + '\'' +
                '}';
    }
}
